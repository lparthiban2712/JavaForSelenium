package java_programs;

public class LoopsStatement {
	
	public static void main(String[] args)
	{
		
		//while loop
		
		int i=1;
		
		while(i<=10)
		{
			System.out.println(i);
			i+=1;
			
		}
		
		
		//do while loop
		
				int j=10;
				
				do
				{
					System.out.println(j);
					j--;
					
				}while(j>0);
				
		//for loop
				for (int k=1;k<=10;k+=2)
				{
					System.out.println(k);
				}
	}

}
